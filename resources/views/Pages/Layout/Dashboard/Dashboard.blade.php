        var clock = document.getElementById('clock');

        function time() {
            var d = new Date();
            var s = d.getSeconds();
            var m = d.getMinutes();
            var h = d.getHours();
            let am_pm = (h >= 12) ? "PM" : "AM";

            if(h >= 12){
                h -=12;
            }

            clock.innerHTML = 
            '<i class="fa-solid fa-clock"></i> ' + ("0" + h).substr(-2) + ":" + ("0" + m).substr(-2) + ":" + ("0" + s).substr(-2) + " " + am_pm;
        }

        setInterval(time, 1000);

        $('#clock').click(function (e) { 
            e.preventDefault();
        });

    </script>

